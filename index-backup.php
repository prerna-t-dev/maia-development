function handleFormSubmission(formId, phpUrl, thankYouUrl, messageFormId) {
    $('#' + formId).submit(function(e) {
        e.preventDefault();
        console.log("Form submission triggered for:", formId);

        var $form = $(this);
        var $submitButton = $form.find('input[type="submit"]');
        var originalButtonText = $submitButton.val();

        // Disable the button and show "Submitting..."
        $submitButton.prop('disabled', true).val("Submitting...");

        var datamz = new FormData(this);
        var phcode = $('.iti__selected-flag').attr('title');
        datamz.append('phcode', phcode);

        console.log("FormData object for", formId, ":", Array.from(datamz.entries()));

        // Forms that skip API request
        const skipApiForms = ['futureteam', 'vendorsform'];

        if (skipApiForms.includes(formId)) {
            console.log(`Skipping API requests for form: ${formId}`);
            
            // Directly trigger PHP email submission
            $.ajax({
                type: 'POST',
                url: phpUrl,
                data: datamz,
                processData: false,
                contentType: false,
                success: function(data) {
                    console.log("PHP form submission successful for form", formId, ":", data);
                    window.location.href = thankYouUrl;
                    $("#" + messageFormId).html("Thank you for your submission. Our team will get back to you shortly!")
                                          .removeClass("red-message").addClass("green-message");
                    document.getElementById(formId).reset();
                },
                error: function(e) {
                    console.error("PHP form submission error for form", formId, ":", e);
                    $("#" + messageFormId).html("Oops! Something went wrong. Please try again.")
                                          .removeClass("green-message").addClass("red-message");
                }
            }).always(function () {
                $submitButton.prop('disabled', false).val(originalButtonText);
            });
        } else {
            // Step 1: Get security token and proceed to lead creation
            TokenSubmitDataonGetMethod(function(tokenId) {
                if (tokenId) {
                    SubmitDataOnPOSTMethod(formId, tokenId, function(apiResponse) {
                        // Step 2: After lead creation, proceed with form submission
                        $.ajax({
                            type: 'POST',
                            url: phpUrl,
                            data: datamz,
                            processData: false,
                            contentType: false,
                            success: function(data) {
                                console.log("PHP form submission successful for form", formId, ":", data);
                                window.location.href = thankYouUrl;
                                $("#" + messageFormId).html("Thank you for your submission. Our team will get back to you shortly!")
                                                      .removeClass("red-message").addClass("green-message");
                                document.getElementById(formId).reset();
                            },
                            error: function(e) {
                                console.error("PHP form submission error for form", formId, ":", e);
                                $("#" + messageFormId).html("Oops! Something went wrong. Please try again.")
                                                      .removeClass("green-message").addClass("red-message");
                            }
                        }).always(function () {
                            $submitButton.prop('disabled', false).val(originalButtonText);
                        });
                    });
                } else {
                    console.error("Failed to retrieve token. Cannot create lead for form", formId);
                    $submitButton.prop('disabled', false).val(originalButtonText);
                }
            });
        }
    });
}

